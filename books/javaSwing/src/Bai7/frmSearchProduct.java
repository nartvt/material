    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        
        model.addColumn("Name");
        model.addColumn("Price");
        model.addColumn("Amount");
        ConnectDatabase conn=new ConnectDatabase();
        try {
            List<Product> listProduct=conn.SearchProduct(txtProductName.getText());
            for(Product pro:listProduct)
            {
                model.addRow(new Object[]{pro.productName,pro.productPrice,pro.amount});
            }
            tblSearchProduct.setModel(model);
            conn.closeConnect();
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(frmSearchProduct.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

