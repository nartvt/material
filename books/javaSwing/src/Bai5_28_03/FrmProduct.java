    Product pro=new Product(LoaiSP,txtmaSP.getText(),txtTenSP.getText(),txtdonVitinh.getText(),Double.parseDouble(txtDonGia.getText()));
    listProduct.add(pro);
    Collections.sort(listProduct, pro.ProCompare);
