private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
// TODO add your handling code here:
    //int choose=cbbLoaiSanPham.getSelectedIndex(),number=0;
   
    String item=String.valueOf(cbbLoaiSanPham.getSelectedItem()), chuoi;
  //  String chuoi;
    model.clear();
    int number=0;
    for(Product p:listProduct)
    {
        if(p.loaiSP.equals(item))
        {
            chuoi=p.maSP+" "+p.tenSP+" "+p.donviTinh+" "+p.giaSP+"\n";
            model.addElement(chuoi);
            number++;
        }
    }
    listInfoAll.setModel(model);
    txtNumber.setText(number+"");
    
}//GEN-LAST:event_btnSearchActionPerformed

