    for(Product pr:listProduct)
    {
        if(pr.maSP.equals(txtmaSP.getText()))
        {
            JOptionPane.showMessageDialog(this,"Ma San Pham Da Co, Nhap Lai !");
            check=true;
            break;
        }
    }
